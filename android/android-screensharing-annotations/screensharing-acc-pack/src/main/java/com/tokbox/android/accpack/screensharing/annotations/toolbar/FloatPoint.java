package com.tokbox.android.accpack.screensharing.annotations.toolbar;


public class FloatPoint {
    public float x, y;

    public FloatPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}